import java.util.*;
public class LessonM{
	static void printLine(int n){
		for(int i=1;i<=n;i++){
			for(int j =0;j<i;j++){
				System.out.print("*");
			}
			System.out.println();
		}
	}
	public static void main(String[] args){
		System.out.print("段を入力>>");
		int n =new Scanner(System.in).nextInt();
		printLine(n);
	}
}
