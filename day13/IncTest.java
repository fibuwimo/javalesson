import java.util.*;
public class IncTest{
	public static void main(String[] args){
		int x =10;
		System.out.println(x++);
		System.out.println(x);
		System.out.println("-----------");
		int y =10;
		System.out.println(++y);
		System.out.println(y);
		System.out.println("-----------");
		int z=10;
		System.out.println(z+1);
		System.out.println(z);
	}
}
